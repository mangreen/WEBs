package edu.ntnu.tesc.module.beans;

public class QeisBudgetYear {
	
	private int autoindex;
	private int budgetYear;
	private int currentPrice;
	private int capitalPrice;
	
	public int getAutoindex() {
		return autoindex;
	}
	
	public void setAutoindex(int autoindex) {
		this.autoindex = autoindex;
	}
	
	public int getBudgetYear() {
		return budgetYear;
	}
	
	public void setBudgetYear(int budgetYear) {
		this.budgetYear = budgetYear;
	}
	
	public int getCurrentPrice() {
		return currentPrice;
	}
	
	public void setCurrentPrice(int currentPrice) {
		this.currentPrice = currentPrice;
	}
	
	public int getCapitalPrice() {
		return capitalPrice;
	}
	
	public void setCapitalPrice(int capitalPrice) {
		this.capitalPrice = capitalPrice;
	}

	
}
